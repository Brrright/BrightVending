    private void fileChoserBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_fileChoserBtnActionPerformed
        // TODO add your handling code here:
        try {
            JFileChooser browseImageFile = new JFileChooser();

            // Filter image extensions
            browseImageFile.addChoosableFileFilter(new filterImg());
            browseImageFile.setAcceptAllFileFilterUsed(false);
            browseImageFile.showOpenDialog(null);
            File selectedImageFile = browseImageFile.getSelectedFile();
            String selectedImagePath = selectedImageFile.getAbsolutePath();

            // Display image on jLable
            ImageIcon image = new ImageIcon(selectedImagePath);
            // Resize image to fit jLabel
            Image img = image.getImage().getScaledInstance(PictureLabel.getWidth(), PictureLabel.getHeight(), Image.SCALE_SMOOTH);
            PictureLabel.setIcon(new ImageIcon(img));

            // move to new path
            String newPath = "src/main/java/com/mycompany/brightvending/image/productImage/";
            imagePath = newPath;
            File directory = new File(imagePath);
            if(!directory.exists()) {
                directory.mkdirs();
            }

            File sourceFile = null;
            File destinationFile = null;
            String extension = selectedImagePath.substring(selectedImagePath.lastIndexOf(".")+1);
            sourceFile = new File(selectedImagePath);
            int uniqueID = CRUDFileHandling.IDgeneration(productfile);
            imagePath = newPath+uniqueID+"newImage." + extension;
            Path path = Paths.get(imagePath);
            if ( Files.exists(path)) {
                File tobeDelete = new File(imagePath);
                tobeDelete.delete();
            }

            destinationFile =  new File (imagePath);
            Files.copy(sourceFile.toPath(), destinationFile.toPath());
            
        } catch (IOException ex) {
            Logger.getLogger(adminManageProductForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_fileChoserBtnActionPerformed

