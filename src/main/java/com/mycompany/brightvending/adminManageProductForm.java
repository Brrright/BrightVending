    private void DoneBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DoneBtnActionPerformed

        int product_id ;
        String product_name, p_price, product_price, product_stock, picture_path, modeForInsert = "";
        double temp;

        product_name = productName.getText();
        p_price = productPrice.getText();
        product_stock = productStock.getText();
        picture_path = imagePath;
        
        // 1) VALIDATE INPUT
        // validate empty
        if (product_name.isEmpty() || p_price.isEmpty() || product_stock.isEmpty() || picture_path.isEmpty() || picture_path == null) {
            JOptionPane.showMessageDialog(null,"Please make sure every input is filled with valid input.","Alert",JOptionPane.WARNING_MESSAGE);
            return;
        }
        // validate if the input is in the correct form or not (if not the labels will have text)
        if (!"".equals(priceInvalidLabel.getText()) || !"".equals(stockInvalidLabel.getText())) {
            JOptionPane.showMessageDialog(null,"Please make sure every input is filled with valid input.","Alert",JOptionPane.WARNING_MESSAGE);
            return;
        }
        temp = Double.parseDouble(p_price);
        product_price = dformat.format(temp);

        // 2) GENERATE ID
        product_id = CRUDFileHandling.IDgeneration(productfile);

        // 3) EITHER INSERT/ UPDATE RECORD
        String[] recordContent = {product_id + ";" + product_name + ";" + product_price +";" + product_stock+ ";" + picture_path};
        String header = "ProductID;ProductName;ProductPrice;Stock;ProductPicture";

        if (null != action) switch (action) {
            case "add":
                modeForInsert="append";
                break;
            case "edit":
                modeForInsert="overwrite";
                break;
            case "":
                JOptionPane.showMessageDialog(null,"action did not specified.","Alert",JOptionPane.WARNING_MESSAGE);
                return;
            default:
                break;
        }
        
        Object[] insert_record_msg = CRUDFileHandling.InsertRecord(productfile, recordContent, header, modeForInsert);
        String msgTitle = (String) insert_record_msg[0];
        String msgContent = (String) insert_record_msg[1];
        if (msgTitle != "ERROR") {
            JOptionPane.showMessageDialog(null, msgContent, msgTitle, JOptionPane.INFORMATION_MESSAGE);
            productName.setText("");
            productPrice.setText("");
            productStock.setText("");
            PictureLabel.setIcon(new ImageIcon(""));
        }
        else{
            JOptionPane.showMessageDialog(null, msgContent, msgTitle, JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_DoneBtnActionPerformed

