    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        // TODO add your handling code here:
        int i = ProductTable.getSelectedRow();
        if (i >=0) {
            int res = JOptionPane.showConfirmDialog(null, "Are you sure you wanted to delete the selected product?");
            if (res == JOptionPane.YES_OPTION) {
//                delete the record
                DefaultTableModel model = (DefaultTableModel) ProductTable.getModel();
                String productID = (String) model.getValueAt(i, 0);
                String path = "src/main/java/com/mycompany/brightvending/files/product.txt";
                Object[] msgReturned = CRUDFileHandling.FetchOneRecord(path, productID);
                for (Object record : msgReturned) {
                    if (record == "ERROR") {
                        JOptionPane.showMessageDialog(null, record);
                        return;
                    }
                }
                Object[] deleteMessage = CRUDFileHandling.DeleteRecord(path, msgReturned);
                if (deleteMessage[0] == "ERROR") {
                    JOptionPane.showMessageDialog(null, deleteMessage[1].toString(), deleteMessage[0].toString() , JOptionPane.WARNING_MESSAGE);
                    return;
                }
//                model.removeRow(i);    
                JOptionPane.showMessageDialog(null, "deleted successfully");

            }
        }
        else {
            JOptionPane.showMessageDialog(null, "Please select a record from the table first.");
        }
    }//GEN-LAST:event_deleteBtnActionPerformed

