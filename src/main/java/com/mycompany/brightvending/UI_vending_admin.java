    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        int res = JOptionPane.showConfirmDialog(null, "**PLEASE MAKE SURE YOU HAVE HIT [ENTER] ON KEY BOARD AFTER U EDIT THE TABLE RECORD**\nAre you sure you wanted to save the change you have made in the table?\n\nNote: The changes made on table will NOT be validate, it might CRASH the system if invalid data is entered.\nPlease make sure every data is valid before you save.");
        if (res == JOptionPane.YES_OPTION) {
            String productFile = "src/main/java/com/mycompany/brightvending/files/product.txt";
            String header = "ProductID;ProductName;ProductPrice;Stock;ProductPicture";
            int numberOfRow = ProductTable.getRowCount();
            int numberOfCol = ProductTable.getColumnCount();
            try {
                FileWriter fwriter = new FileWriter(productFile, false);
                BufferedWriter bwriter= new BufferedWriter(fwriter);
                bwriter.write(header+"\n");
                for (int i=0; i<numberOfRow; i++) {
                    for (int j=0; j<numberOfCol;j++) {
                        if(j == numberOfCol-1) {
                            String dataRecord= ProductTable.getModel().getValueAt(i,j).toString();
                            bwriter.write(dataRecord);
                        } 
                        else {
                        // convert data into string
                        String dataRecord= ProductTable.getModel().getValueAt(i,j).toString()+ ";";
                        bwriter.write(dataRecord);     
                    }
                    }
                    bwriter.newLine();
                }
                bwriter.close();
                fwriter.close();
            }
            catch (IOException ex) {
                Logger.getLogger(UI_vending_admin.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }
    }//GEN-LAST:event_saveBtnActionPerformed

